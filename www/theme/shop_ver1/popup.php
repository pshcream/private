- 개인정보는 비밀번호에 의해 보호되며, 중요한 데이터는 별도의 보안(암호화)기능을 통해 보호되고 있습니다.
- 백신프로그램을 이용하여 컴퓨터바이러스에 의한 피해를 방지하기 위한 조치를 취하고 있습니다.- 회사는 암호알고리즘을 이용하여 네트워크 상의 개인정보를 안전하게 전송할 수
있는 보안장치(SSL)를 채택하고 있습니다.
- 해킹 등 외부침입에 대비하여 침입차단시스템을 운영하고 있습니다.

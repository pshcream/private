알차다(이하 “회사”라고 함)은 고객님의 개인정보를 중요시하며, ‘정보통신망 이용촉진 및 정보보호에 관한 법률'을 준수하고 있습니다.
회사는 <개인정보처리방침>을 통하여
고객님께서 제공하시는 개인정보가 어떠한 용도와 방식으로 이용되고 있으며, 개인정보보호를 위해 어떠한 조치가 취해지고 있는지 알려드립니다. <br>
회사는 개인정보취급방침을 개정하는
경우 웹사이트를 통하여 공지(또는 개별공지)할 것입니다.

개인정보취급방침은 다음과 같은 내용을 담고 있습니다.
- 수집하는 개인정보 항목 및 수집방법
- 수집한 개인정보의 위탁
- 개인정보의 수집 및 이용목적
- 이용자 및 법정대리인의 권리와 그 행사방법
- 개인정보의 보유 및 이용기간
- 개인정보의 기술적/관리적 보호대책- 개인정보의 파기절차 및 방법
- 개인정보 자동수집 장치의 설치, 운영 및 그 거부에 관한 사항
- 개인정보 제공
- 개인정보에 관한 민원서비스


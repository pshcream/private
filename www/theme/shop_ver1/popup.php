성명, 휴대폰번호, 전화번호, 생년월일, 이메일

[개인정보 수집방법]
회사는 다음과 같은 방법으로 개인정보를 수집합니다.
홈페이지를 통한 사용자 질문과 답변 등의 민원처리,인터넷 상담예약접수 및 상품설명 등의 민원처리, 생성정보수집툴을 통한 수집

[ 개인정보의 수집 및 이용목적 ]
회사는 수집한 개인정보를 다음의 목적을 위해 활용합니다.
- 사용자 질의 대응
- 상담, 계약안내, 자사 상품 광고 및 정보 안내

[ 개인정보의 보유 및 이용기간 ]
회사는 개인정보의 수집 및 이용 목적이 달성된 후에는 해당 정보를 지체 없이 파기합니다. 다만, 다음의 정보에 대해서는 아래의 이유로 명시한 기간 동안
보존합니다.웹사이트에서 수집하는 항목
- 보유 항목 : 성명, 휴대폰번호, 전화번호, 생년월일,이메일
- 보유 기간 : 1년
- 보유 이유 : 사용자 식별, 사용자 질의 대응, 인터넷 예약 접수 관리

[ 개인정보의 파기절차 및 방법 ]
회사는 원칙적으로 개인정보 수집 및 이용목적이 달성된 후에는 해당 정보를 지체 없이 파기합니다. 파기절차 및 방법은 다음과 같습니다.
▶파기절차
고객님이 입력하신 정보는 목적이 달성된 후 별도의 DB로 옮겨져(종이의 경우 별도의 서류함) 내부 방침 및 기타 관련 법령에 의한 정보보호 사유에 따라(보유 및 이용기간
참조) 일정 기간 저장된 후 파기됩니다. 별도 DB로 옮겨진 개인정보는 법률에 의한 경우가 아니고서는 보유되어지는 이외의 다른 목적으로 이용되지 않습니다.
▶파기방법
- 전자적 파일형태로 저장된 개인정보는 기록을 재생할 수 없는 기술적 방법을 사용하여 삭제합니다.
- 종이에 출력된 개인정보는 분쇄기로 분쇄하거나 소각을 통하여 파기합니다.

[ 개인정보의 제공 ]
회사는 이용자의 개인정보를 원칙적으로 외부에 제공하지 않습니다. 다만, 아래의 경우에는 예외로 합니다.
- 이용자들이 사전에 동의한 경우
- 법령의 규정에 의거하거나, 수사 목적으로 법령에 정해진 절차와 방법에 따라 수사기관의 요구가 있는 경우
- 이용자 또는 그 법정대리인이 의사표시를 할 수 없는 상태에 있거나 주소불명 등으로 사전 동의를 받을 수 없는 경우로서 명백히 이용자 또는 제3자의 급박한 생명, 신체,
재산의 이익을 위하여 필요하다고 인정되는 경우

[ 이용자 및 법정대리인의 권리와 그 행사방법 ]
이용자 및 법정 대리인은 언제든지 등록되어 있는 자신 혹은 당해 만 21세 미만 개인정보를 조회하거나 수정할 수 있으며, 동의철회를 요청할 수 있습니다. 귀하가 개인정보의
오류에 대한 정정을 요청하신 경우에는 정정을 완료하기 전까지 당해 개인정보를 이용 또는 제공하지 않습니다. 또한 잘못된 개인정보를 제3자에게 이미 제공한 경우에는 정정
처리결과를 제3자에게 지체 없이 통지하여 정정이 이루어지도록 하겠습니다.
회사는 다음에 해당하는 경우에는 개인정보의 전부 또는 일부에 대하여 열람 또는 정정을 거절할 수 있습니다.
- 본인 또는 제3자의 생명, 신체, 재산 또는 권익을 현저하게 해할 우려가 있는 경우
- 회사의 업무에 현저한 지장을 미칠 우려가 있는 경우
